package Unit_2;

public class U2P14 {
    public int num1, num2;
    public U2P14(){
        num1 = 100;
        num2 = 200;
    }
    public void relationalOperators(){
        System.out.println("Numbers are " + num1 + " " + num2 );
        System.out.println(num1+" ==  "+num2 +" " + (num1==num2) );
        System.out.println(num1+" !=  "+num2 +" " + (num1!=num2) );
        System.out.println(num1+" >=  "+num2 +" " + (num1>=num2) );
        System.out.println(num1+" <=  "+num2 +" " + (num1<=num2) );
    }
}
