package Unit_2;

public class U2P13 {
    public int a, b;
    public U2P13(){
        a = 123;
        b = 15;
    }
    public void arithmeticOperators(){
        System.out.println(a + " + " + b + " = " + (a+b) );
        System.out.println(a + " - " + b + " = " + (a-b) );
        System.out.println(a + " * " + b + " = " + (a*b) );
        System.out.println(a + " / " + b + " = " + (a/b) );
        System.out.println(a + " % " + b + " = " + (a%b) );
    }
}
